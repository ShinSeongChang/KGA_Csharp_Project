        public void Draw_Title()
        {
            Scene draw_Ui = new Scene();
            MainPlay init_Game = new MainPlay();

            Console.SetCursorPosition(100, 20);
            Console.Write("Title Scene");
            Console.SetCursorPosition(95, 30);
            Console.WriteLine("Enter 키를 눌러 입장");
            Console.SetCursorPosition(95, 32);
            Console.WriteLine("ESC 키를 눌러 게임 종료");
            Console.WriteLine();

            while (true)
            {
                ConsoleKeyInfo game_Init = Console.ReadKey();

                if(game_Init.Key == ConsoleKey.Escape)
                {
                    Draw_Sene();

                    Console.SetCursorPosition(100, 25);                    
                    Console.WriteLine("게임을 종료합니다...");
                    Console.SetCursorPosition(100, 27);

                    return;
                }
                else if (game_Init.Key == ConsoleKey.Enter)
                {
                    Draw_Sene();

                    Console.SetCursorPosition(100, 25);
                    Console.WriteLine("게임 입장");
                    Thread.Sleep(500);

                    init_Game.Play_Game();
                    
                    // 메인 플레이중 게임종료를 눌렀을시
                    draw_Ui.Draw_Sene();
                    draw_Ui.Draw_Title();
                    return;
                    // 메인 플레이중 게임종료를 눌렀을시
                }
                else
                {
                    Draw_Sene();

                    Console.SetCursorPosition(100, 27);
                    Console.Write("키 입력이 올바르지 않습니다.");
                    Console.SetCursorPosition(100, 29);
                    Console.Write("게임 입장 : Enter");
                    Console.SetCursorPosition(100, 31);
                    Console.Write("게임 종료 : ESC");

                }


            }


        }

