        public void Draw_Title()
        {
            MainPlay init_Game = new MainPlay();
            Draw_TitleInfo();

            Console.SetCursorPosition(93, 44);
            Console.WriteLine("Enter 키를 눌러 입장");
            Console.SetCursorPosition(93, 46);
            Console.WriteLine("ESC 키를 눌러 게임 종료");
            Console.WriteLine();

            while (true)
            {
                ConsoleKeyInfo game_Init = Console.ReadKey();

                if (game_Init.Key == ConsoleKey.Escape)
                {
                    Draw_Scene();

                    Console.SetCursorPosition(100, 25);
                    Console.WriteLine("게임을 종료합니다...");
                    Console.SetCursorPosition(100, 27);

                    return;
                }
                else if (game_Init.Key == ConsoleKey.Enter)
                {
                    

                    for(int i = 0; i <2; i++)
                    {
                        Draw_Scene();
                        Console.SetCursorPosition(100, 25);
                        Console.Write("게임 입장중....");

                        Thread.Sleep(500);

                        Draw_Scene();
                        Console.SetCursorPosition(100, 25);
                        Console.Write("게임 입장중...");
                        Thread.Sleep(500);
                    }
                    
                    

                    init_Game.Play_Game();

                    // 메인 플레이중 게임종료를 눌렀을시
                    Draw_Scene();
                    Draw_TitlePainting();
                    Draw_Title();
                    return;
                    // 메인 플레이중 게임종료를 눌렀을시
                }
                else
                {

                    Console.SetCursorPosition(93, 44);
                    Console.WriteLine("Enter 키를 눌러 입장");
                    Console.SetCursorPosition(93, 46);
                    Console.WriteLine("ESC 키를 눌러 게임 종료");
                    Console.SetCursorPosition(93, 48);
                    Console.Write("키 입력이 올바르지 않습니다.");
                    Console.WriteLine();

                }

            }

        }

